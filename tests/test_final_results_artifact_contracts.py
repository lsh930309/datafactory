from __future__ import annotations

import json
from pathlib import Path
from typing import Any

import pytest

ROOT = Path(__file__).resolve().parents[1]
RESULTS_ROOT = ROOT / "outputs" / "results"


def _json(path: Path) -> Any:
    return json.loads(path.read_text(encoding="utf-8"))


def _bbox_leaves(value: Any, prefix: tuple[str, ...] = ()) -> dict[tuple[str, ...], dict[str, Any]]:
    if not isinstance(value, dict):
        return {}
    if {"l", "t", "r", "b"}.issubset(value.keys()):
        return {prefix: value}
    leaves: dict[tuple[str, ...], dict[str, Any]] = {}
    for key, child in value.items():
        child_path = (*prefix, str(key))
        leaves.update(_bbox_leaves(child, child_path))
    return leaves


def _value_at_path(payload: Any, path: tuple[str, ...]) -> Any:
    cursor = payload
    for part in path:
        if isinstance(cursor, dict) and part in cursor:
            cursor = cursor[part]
        elif isinstance(cursor, list) and part.isdigit() and int(part) < len(cursor):
            cursor = cursor[int(part)]
        else:
            return None
    return cursor


def _legacy_slash_value(payload: Any, path: tuple[str, ...]) -> Any:
    """Resolve pre-migration bbox paths that split literal slash-bearing keys.

    Real outputs are intentionally not regenerated by code changes. Current
    exporter behavior is locked separately by the literal-slash unit test.
    """

    if not path:
        return payload
    if not isinstance(payload, dict):
        return None
    for end in range(len(path), 0, -1):
        joined = "/".join(path[:end])
        if joined in payload:
            return _legacy_slash_value(payload[joined], path[end:])
    return None


def _is_nonempty_scalar(value: Any) -> bool:
    return value is not None and not isinstance(value, (dict, list)) and str(value).strip() != ""


def _sample_gt_paths() -> list[Path]:
    if not RESULTS_ROOT.exists():
        return []
    return sorted(
        path
        for path in RESULTS_ROOT.glob("*/*/sample_*.json")
        if not path.name.endswith("-bbox.json") and not path.name.endswith("-pii.json")
    )


def test_final_results_bbox_only_points_to_rendered_nonempty_gt_values() -> None:
    """Final bbox is an actual rendered glyph bbox, never a source/review fallback.

    If a bbox leaf exists, the same semantic path must exist in the paired GT
    JSON and its value must be a non-empty scalar.  Empty checkboxes, skipped
    values, object/list nodes, and stale flat-path bboxes are all invalid.
    """

    summary_path = RESULTS_ROOT / "_run_summary.json"
    if summary_path.exists() and int((_json(summary_path) or {}).get("schema_version") or 1) < 2:
        pytest.skip("outputs/results predates structured semantic-path export; real outputs are not regenerated by implementation tests")
    gt_paths = _sample_gt_paths()
    if not gt_paths:
        pytest.skip("outputs/results has no generated sample JSON files")

    violations: list[str] = []
    checked_bbox_leaves = 0
    for gt_path in gt_paths:
        bbox_path = gt_path.with_name(f"{gt_path.stem}-bbox.json")
        if not bbox_path.exists():
            continue
        gt_payload = _json(gt_path)
        bbox_payload = _json(bbox_path)
        for semantic_path, bbox in _bbox_leaves(bbox_payload).items():
            checked_bbox_leaves += 1
            value = _value_at_path(gt_payload, semantic_path)
            if not _is_nonempty_scalar(value):
                value = _legacy_slash_value(gt_payload, semantic_path)
            if not _is_nonempty_scalar(value):
                violations.append(f"{bbox_path.relative_to(ROOT)}::{semantic_path} -> GT value {value!r}")
            for axis in ("l", "t", "r", "b"):
                coord = bbox.get(axis)
                if not isinstance(coord, (int, float)) or not 0 <= float(coord) <= 1:
                    violations.append(f"{bbox_path.relative_to(ROOT)}::{semantic_path}.{axis} invalid coord {coord!r}")
            if all(isinstance(bbox.get(axis), (int, float)) for axis in ("l", "t", "r", "b")):
                if float(bbox["l"]) >= float(bbox["r"]) or float(bbox["t"]) >= float(bbox["b"]):
                    violations.append(f"{bbox_path.relative_to(ROOT)}::{semantic_path} invalid ltrb order {bbox!r}")

    assert checked_bbox_leaves > 0, "no final result bbox leaves were checked"
    assert not violations, "invalid final-result bbox contract:\n" + "\n".join(violations[:80])


def _result_schema_paths() -> list[Path]:
    if not RESULTS_ROOT.exists():
        return []
    return sorted(RESULTS_ROOT.glob("*/*/schema.json"))


def _contains_key(payload: Any, key: str) -> bool:
    if isinstance(payload, dict):
        return key in payload or any(_contains_key(child, key) for child in payload.values())
    if isinstance(payload, list):
        return any(_contains_key(child, key) for child in payload)
    return False


def _leaf_keys(payload: Any) -> set[str]:
    if not isinstance(payload, dict):
        return set()
    output: set[str] = set()
    for key, child in payload.items():
        if isinstance(child, dict):
            output.update(_leaf_keys(child))
        else:
            output.add(str(key))
    return output


def test_final_results_explicit_pii_files_only_reference_gt_leaf_keys() -> None:
    pii_paths = sorted(RESULTS_ROOT.glob("*/*/sample_*-pii.json")) if RESULTS_ROOT.exists() else []
    if not pii_paths:
        pytest.skip("outputs/results has no explicit PII files yet")
    violations: list[str] = []
    for pii_path in pii_paths:
        gt_path = pii_path.with_name(pii_path.name.replace("-pii.json", ".json"))
        pii = _json(pii_path)
        if not isinstance(pii, list) or any(not isinstance(key, str) or not key for key in pii):
            violations.append(f"{pii_path.relative_to(ROOT)} is not a non-empty string key list")
            continue
        if not gt_path.exists():
            violations.append(f"{pii_path.relative_to(ROOT)} has no paired GT")
            continue
        unknown = set(pii) - _leaf_keys(_json(gt_path))
        if unknown:
            violations.append(f"{pii_path.relative_to(ROOT)} unknown GT keys: {sorted(unknown)}")
    assert not violations, "invalid explicit PII contract:\n" + "\n".join(violations)


def test_final_results_do_not_embed_machine_metadata_keys() -> None:
    """GT labels and primary schemas are semantic labels, not sample metadata."""

    paths = [*_result_schema_paths(), *_sample_gt_paths()]
    if not paths:
        pytest.skip("outputs/results has no generated schema/sample JSON files")

    forbidden = {"doc_id", "document_id", "document_name", "sample_id", "schema_version", "source_image", "source_inpainted"}
    violations: list[str] = []
    for path in paths:
        payload = _json(path)
        for key in forbidden:
            if _contains_key(payload, key):
                violations.append(f"{path.relative_to(ROOT)} contains metadata key {key!r}")

    assert not violations, "metadata leaked into final GT/schema:\n" + "\n".join(violations[:80])
